    public String showDialog(JFrame frame){
        String codigo = null;
        if( dialog == null){
            // cria um novo dialog
            dialog = new JDialog(frame, "Insere Comando Move", true);
            // adiciona este painel no dialog
            dialog.add(this);
            dialog.pack();
            Dimension tela =  Toolkit.getDefaultToolkit().getScreenSize();  
            dialog.setLocation( (tela.width - dialog.getSize().width)/2, 
                    (tela.height - dialog.getSize().height)/2 );
        }
        // mostra o dialog e segura a execução até o fechamento do mesmo
        dialog.setVisible(true);
        // se confirmou
        if(gerarCodigo){
            // cria nova string
            codigo = "";
            codigo += painelSelecionaPeca1.getSelected() + ".move(" +
                    String.valueOf(x) + ", " +
                    String.valueOf(y) + ", " +
                    String.valueOf(z) + ")";
        }
        return codigo;
    }

    private boolean validaEntrada() {
        try{
            x = Float.parseFloat(jtX.getText());
        } catch(Exception e) {
            Msg.erro("Entrada inválida!");
            jtX.requestFocus();
            return false;
        }
        try{
            y = Float.parseFloat(jtY.getText());
        } catch(Exception e) {
            Msg.erro("Entrada inválida!");
            jtY.requestFocus();
            return false;
        }
        try{
            z = Float.parseFloat(jtZ.getText());
        } catch(Exception e) {
            Msg.erro("Entrada inválida!");
            jtZ.requestFocus();
            return false;
        }
        if(painelSelecionaPeca1.getSelected() == null){
            Msg.erro("Entrada inválida!");
            painelSelecionaPeca1.requestFocus();
            return false;
        }
        
        return true;
    }
    
