    private boolean validaEntrada(){
        if (cor == null){
            Msg.erro("Escolha uma cor!");
            return false;
        }
        if(painelSelecionaPeca1.getSelected() == null){
            Msg.erro("Entrada inv·lida!");
            painelSelecionaPeca1.requestFocus();
            return false;
        }
        return true;
    }
