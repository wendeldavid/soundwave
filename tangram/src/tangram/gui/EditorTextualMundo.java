    private void jtaCodigoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_jtaCodigoCaretUpdate
        Element root = jtaCodigo.getDocument().getDefaultRootElement();
        dot = evt.getDot();
        line = root.getElementIndex( dot );
        lineStartOffset = root.getElement( line ).getStartOffset();
        lineEndOffset = root.getElement( line ).getEndOffset();
        col = dot - lineStartOffset;
        
        jbLinha.setText((line+1) + ":" + (col+1));
    }//GEN-LAST:event_jtaCodigoCaretUpdate

    private void jbtPlayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtPlayActionPerformed
        try{
            Compiler compiler = new Compiler(ReadWriteFile.read(file));
            final ModeloExecutavel me = compiler.compileModel();
            me.setFigura(figura);
            new Thread(){
                @Override
                public void run(){
                    me.execute((String)jcbAcoes.getSelectedItem());
                }
            }.start();
        } catch (AnalysisError ex){
            Msg.aviso(ex.getMessage());
        }
}//GEN-LAST:event_jbtPlayActionPerformed

