    public void insereComando(String comando){
        String text = jtaCodigo.getText();
        String newText;
        int posicaoFinal = lineEndOffset - 1;
        
        if(lineEndOffset == 0)
            newText = comando + text;
        else
            newText = text.substring(0, lineEndOffset - 1) + 
                    (lineEndOffset-1 != lineStartOffset ? "\n" : "") +
                    comando + text.substring(lineEndOffset - 1);
        jtaCodigo.setText(newText);
        jtaCodigo.requestFocus();
        try{
            jtaCodigo.setCaretPosition(posicaoFinal + 1);
            mundo.getEditorGrafico().requestFocus();
        } catch (Exception e){}
    }
    
    private void preExecuta(){
        MundoExecutavel exe = mundo.Compila();
        if (exe == null)
            return;
        
        //
        exe.start();
    }
    
    public void setSelectedModel(String modelName,
            ArrayList<String>modelAction, File file, Figura figura){
        jcbAcoes.removeAllItems();
        if(modelName != null){
            lbModelName.setText(modelName);
            for (String s : modelAction ){
                jcbAcoes.addItem(s);
            }
            this.file = file;
            this.figura = figura;
        } else
            lbModelName.setText("Nenhum Modelo Selecionado");
    }
    
